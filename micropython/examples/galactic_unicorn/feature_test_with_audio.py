def next_beat():
    global beat
    for i in range(5):
        if notes[i][beat] > 0:
            channels[i].freq(notes[i][beat])
            channels[i].trigger_attack()
        elif notes[i][beat] == -1:
            channels[i].trigger_release()

    beat = (beat + 1) % SONG_LENGTH


def tick(timer):
    next_beat()


